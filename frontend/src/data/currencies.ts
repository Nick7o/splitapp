export const formatMoney = (amount: number, code: string, locale = i18n.language): string => {
  try {
    return new Intl.NumberFormat(locale, { style: 'currency', currency: code }).format(amount);
  } catch {
    return `${amount.toFixed(2)} ${code}`;
  }
};
